package com.rtc.variable;

public class VariableDemo1 {
    public static void main(String[] args) {
        // 学会定义变量
        // 数据类型 变量名 = 初始值;
        double money = 6.0;
        System.out.println(money);

        // 收红包(从等号右边往左看)
        money = money + 4.0;
        System.out.println(money);


        System.out.println("------------");
        int age = 21;
        System.out.println(age);

        age = 25;
        System.out.println(age);
    }
}
